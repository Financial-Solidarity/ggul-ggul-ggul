// Mock search function
export const mockSearchNftSellList = async (
  keyword: string,
  searchCriteria: {
    name?: string;
    minStatus?: number;
    maxStatus?: number;
    minPrice?: number;
    maxPrice?: number;
  },
  pageNumber: number,
  pageSize: number,
): Promise<{
  content: FoodNftSellDTO[];
  totalElements: number;
  totalPages: number;
}> => {
  return new Promise((resolve) => {
    setTimeout(() => {
      // Filter by search criteria
      let filteredContent = mockFoodNfts;

      if (keyword) {
        filteredContent = filteredContent.filter((nft) =>
          nft.name.toLowerCase().includes(keyword.toLowerCase()),
        );
      }

      if (searchCriteria.minStatus !== undefined) {
        filteredContent = filteredContent.filter(
          (nft) => nft.status >= searchCriteria.minStatus!,
        );
      }

      if (searchCriteria.maxStatus !== undefined) {
        filteredContent = filteredContent.filter(
          (nft) => nft.status <= searchCriteria.maxStatus!,
        );
      }

      if (searchCriteria.minPrice !== undefined) {
        filteredContent = filteredContent.filter(
          (nft) => nft.price >= searchCriteria.minPrice!,
        );
      }

      if (searchCriteria.maxPrice !== undefined) {
        filteredContent = filteredContent.filter(
          (nft) => nft.price <= searchCriteria.maxPrice!,
        );
      }

      const totalElements = filteredContent.length;
      const totalPages = Math.ceil(totalElements / pageSize);

      // Pagination
      const start = pageNumber * pageSize;
      const end = start + pageSize;
      const content = filteredContent.slice(start, end);

      resolve({ content, totalElements, totalPages });
    }, 500);
  });
};

